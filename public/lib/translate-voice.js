function getPhoneme(msg, voice) {
	const words = PHONEME[voice];
	if (!words) {
		return msg;
	}
	const m = msg.split(" ").map(word => {
		return words[word] || word;
	}).join(" ").replaceAll("' ", "'");
//	console.log(`window.speechSynthesis.speak(engine.getUterrance("${m}", "Thomas"));`);
	return m;
}